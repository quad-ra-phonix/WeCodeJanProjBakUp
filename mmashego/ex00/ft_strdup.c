#include <stdlib.h>

char *ft_strdup(const char *src)
{
	char *c;


	c = (char*)malloc(sizeof(*src));
	
	while(*c != 0)
	{
		*c = *src;
		c++;
		src++;
	}
	*c = *src;
	return(c);
}

