int ft_sqrt(int nb);

int ft_sqrt(int nb)
{
  int val;
  val =1;

  if (nb ==0)
    {
      return(0);
    }

  while(val*val < nb)
    {
      val++;
    }

  if ((nb%val)==0)
    {
      return(val);
    }
  else
    {
    return(0);
    }
}

